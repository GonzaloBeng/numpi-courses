CREATE TABLE MASCOTAS (
ID INT NOT NULL,
NOMBRE VARCHAR(20),
ID_DUEﾃ前 INT NOT NULL,
SERVICIO VARCHAR(20),
PRIMARY KEY (ID)
);

CREATE TABLE PERSONAS (
ID INT NOT NULL,
NOMBRE VARCHAR(20),
APELLIDO VARCHAR(20),
COD_ZIP INT NOT NULL,
TLF INT NOT NULL,
PRIMARY KEY (ID)
);

CREATE TABLE DIRECCIONES (
ZIP INT NOT NULL,
NOMBRE_ZONA VARCHAR(20),
RUTA VARCHAR(20)
);

